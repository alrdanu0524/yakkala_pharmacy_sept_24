    private void print_bill_concurrently() {
        Runnable printTask = new Runnable() {
            @Override
            public void run() {
                print(s_id);
            }

        };
        Thread thread = new Thread(printTask);
        thread.start();
    }

    private void print(String s_id) {
        try {
            InputStream in = new FileInputStream("src/service_2inch.jrxml");
//            InputStream in = new FileInputStream("src/invoice_A4.jrxml");

            JasperReport jr = JasperCompileManager.compileReport(in);

            Map m = new HashMap();
            m.put("invo", s_id);
            JasperPrint jp = JasperFillManager.fillReport(jr, m, MC_DB.myConnection());
            JasperPrintManager.printReport(jp, false);

            invo();
        } catch (Exception ex) {
            if (ex.getMessage().contains("Invalid page index range")) {
//                JOptionPane.showMessageDialog(this, "This Invoice Number Can't Find -" + invo);
            } else {
                ex.printStackTrace();
            }
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

