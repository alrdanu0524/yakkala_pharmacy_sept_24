    private void set_details(String id) {
        try {
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM expenses WHERE id = '" + id + "'");
            if (rs.next()) {
                txt_description.setText(rs.getString("discription"));
                txt_title.setText(rs.getString("title"));
                txt_amount.setText(rs.getString("amount"));
                String date = rs.getString("date");
                Calendar c = Calendar.getInstance();
                Date d = sdf.parse(date);
                c.setTime(d);
                dateChooserCombo1.setSelectedDate(c);
            } else {
                txt_description.setText("");
                txt_title.setText("");
                txt_amount.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

