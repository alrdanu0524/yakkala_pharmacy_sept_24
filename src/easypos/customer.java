    public void setDetails(String ids) {

        try {
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM customer WHERE id = '" + ids + "'");
            if (rs.next()) {
                txt_name.setText(rs.getString("name"));
                jTextField5.setText(rs.getString("contact"));
                jTextField6.setText(rs.getString("address"));
                jTextField7.setText(rs.getString("email"));
                jTextArea2.setText(rs.getString("description"));
                txt_credit.setText(rs.getString("credit_amount"));
            } else {

                jTextArea2.setText("");
                txt_search.setText("");
                txt_name.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");

            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }

