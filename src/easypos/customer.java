    public void tbups() {

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM customer");
            NumberFormat numberFormat = NumberFormat.getNumberInstance(Locale.US);
            numberFormat.setMinimumFractionDigits(2);
            numberFormat.setMaximumFractionDigits(2);
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("address"));
                v.add(rs.getString("email"));
                v.add(rs.getString("date"));
                v.add(rs.getString("description"));
                double creditAmount = rs.getDouble("credit_amount");
                String formattedCreditAmount = numberFormat.format(creditAmount);
                v.add(formattedCreditAmount);

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

