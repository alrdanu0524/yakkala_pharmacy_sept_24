    public void product_select() {
        jTable1.addKeyListener(new KeyAdapter() {
            public void keyPressed(KeyEvent evt) {

                if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                    id = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();

                    setDetails(id);
                    txt_name.grabFocus();
                    txt_name.selectAll();

                }
            }

        });

    }

