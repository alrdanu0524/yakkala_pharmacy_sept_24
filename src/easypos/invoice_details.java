    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        String key = "%" + jTextField1.getText().trim() + "%";
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM invoice WHERE inv_no LIKE '" + key + "' ||"
                    + " cus_name LIKE '" + key + "' || type LIKE '" + key + "' || date LIKE '" + key + "' ||"
                    + " user LIKE '" + key + "' || terminal LIKE '" + key + "' ");
            NumberFormat numberFormat = new DecimalFormat("#,##0.00");
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("inv_no"));
                v.add(rs.getString("cus_name"));
                v.add(numberFormat.format(rs.getDouble("total")));
                v.add(rs.getString("type"));
                v.add(rs.getString("date"));
                v.add(rs.getString("time"));
                v.add(numberFormat.format(rs.getDouble("qtys")));
                v.add(rs.getString("user"));
                v.add(rs.getString("terminal"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

