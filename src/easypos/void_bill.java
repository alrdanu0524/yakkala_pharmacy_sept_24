    public void tbups() {

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM invoice");
            NumberFormat numberFormat = NumberFormat.getNumberInstance(Locale.US);
            numberFormat.setMinimumFractionDigits(2);
            numberFormat.setMaximumFractionDigits(2);
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("inv_no"));
                v.add(rs.getString("cus_name"));
                v.add(rs.getString("date"));
                v.add(rs.getString("user"));
                double creditAmount = rs.getDouble("total");
                String formattedCreditAmount = numberFormat.format(creditAmount);
                v.add(formattedCreditAmount);

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

