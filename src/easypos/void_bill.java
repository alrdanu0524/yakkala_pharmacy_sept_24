    private void find_invoiceDetails(String s_id) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        try {
            ResultSet rs_invoice = MC_DB.search_dataQuery("SELECT * FROM invoice_repeated WHERE invo_no = '" + s_id + "'");
            NumberFormat numberFormat = NumberFormat.getNumberInstance(Locale.US);
            numberFormat.setMinimumFractionDigits(2);
            numberFormat.setMaximumFractionDigits(2);
            dtm.setRowCount(0);
            while (rs_invoice.next()) {

                Vector v = new Vector();

                v.add(rs_invoice.getString("pro_id"));
                v.add(rs_invoice.getString("code"));
                v.add(rs_invoice.getString("name"));
                v.add(rs_invoice.getString("price"));
                v.add(rs_invoice.getString("qty"));
                v.add(rs_invoice.getString("free"));
                double creditAmount = rs_invoice.getDouble("sub_cost");
                String formattedCreditAmount = numberFormat.format(creditAmount);
                v.add(formattedCreditAmount);

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
