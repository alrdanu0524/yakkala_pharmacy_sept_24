    public void setDetails(String co) {

        try {
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM supplier WHERE id = '" + co + "'");
            if (rs.next()) {
                txt_name.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("company"));
                jTextField5.setText(rs.getString("contact"));
                jTextField6.setText(rs.getString("address"));
                jTextField7.setText(rs.getString("email"));
                jTextArea1.setText(rs.getString("product"));
                jTextArea2.setText(rs.getString("credit_amount"));
            } else {
                jTextArea1.setText("");
                jTextArea2.setText("");
                txt_search.setText("");
                txt_name.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

