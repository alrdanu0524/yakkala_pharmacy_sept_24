    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        String key = "%" + txt_search.getText().trim() + "%";

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM supplier WHERE name like '" + key + "' or company like '" + key + "' or contact like '" + key + "' or address like '" + key + "' or email like '" + key + "' or product like '" + key + "' ");
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("company"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("address"));
                v.add(rs.getString("email"));
                v.add(rs.getString("product"));
                v.add(rs.getString("credit_amount"));
                v.add(rs.getString("date"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
            e.printStackTrace();
            }
        }
    }//GEN-LAST:event_txt_searchKeyReleased

