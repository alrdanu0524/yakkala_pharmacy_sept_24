    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
 
        String key = "%" + jTextField1.getText() + "%";

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM customer where id like '" + key + "' or name like '" + key + "' or contact like '" + key + "' or address like '" + key + "' or email like '" + key + "' or description like '" + key + "' or date like '" + key + "'");
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("address"));
                v.add(rs.getString("email"));
                v.add(rs.getString("date"));
                v.add(rs.getString("description"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
            e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

