    private void find_item_history(String s_item_code) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            dtm.setRowCount(0);
            DecimalFormat df = new DecimalFormat("#,##0.00");
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM grn_details LEFT "
                    + "JOIN grn_item_details ON grn_details.grn_no = grn_item_details.grn_no "
                    + "WHERE grn_item_details.code = '" + s_item_code + "' ORDER BY grn_details.grn_no DESC");

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("grn_details.grn_no"));
                row.add(rs.getString("grn_details.date"));
                double cost = rs.getDouble("grn_item_details.cost");
                String formattedCost = df.format(cost);
                row.add(formattedCost);

                dtm.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

