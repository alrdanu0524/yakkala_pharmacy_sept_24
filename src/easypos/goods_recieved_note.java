    public void saveTemp() {
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(JLabel.RIGHT);
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        s_code = txt_code.getText().trim();
        s_name = txt_name.getText().trim();
        s_scode = txt_s_code.getText().trim();
        s_brand = txt_brand.getText().trim();

        s_reorder = txt_reorder.getText().trim();
        s_qty = txt_qty.getText().trim();
        s_type = combo_type.getSelectedItem().toString();
        s_rack = txt_rack.getText().trim();
        s_free = txt_free.getText().trim();
        s_sinhala = txt_sinhala.getText().trim();
        s_exp_date = sdf.format(dc_exp_date.getDate());

        s_qty = txt_qty.getText().trim();
        s_cost = String.format("%.2f", Double.parseDouble(txt_cost.getText().trim()));
        s_mrp = String.format("%.2f", Double.parseDouble(txt_mrp.getText().trim()));
        s_sale = String.format("%.2f", Double.parseDouble(txt_sale.getText().trim()));

        d_qty = Double.parseDouble(txt_qty.getText().trim());
        d_cost = Double.parseDouble(txt_cost.getText().trim());
        d_cost_sub = d_qty * d_cost;

        Vector v = new Vector();

        v.add(s_code);
        v.add(s_name);
        v.add(s_scode);
        v.add(s_brand);
        v.add(s_cost);
        v.add(s_sale);
        v.add(s_mrp);
        v.add(s_reorder);
        v.add(s_qty);
        v.add(s_free);
        v.add(s_type);
        v.add(s_rack);
        v.add(String.format("%.2f", d_cost_sub));
        v.add(s_sinhala);
        v.add(s_exp_date);
    
        dt.addRow(v);
        

        int lastRow = jTable1.getRowCount() - 1;

        if (lastRow >= 0) {
            jTable1.setRowSelectionInterval(lastRow, lastRow);
            jTable1.scrollRectToVisible(jTable1.getCellRect(lastRow, 0, true));
        }
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(4).setCellRenderer(rightRenderer);
            jTable1.getColumnModel().getColumn(9).setCellRenderer(rightRenderer);
            jTable1.getColumnModel().getColumn(8).setCellRenderer(rightRenderer);
            jTable1.getColumnModel().getColumn(12).setCellRenderer(rightRenderer);
        }
        clear_tf();
    }

