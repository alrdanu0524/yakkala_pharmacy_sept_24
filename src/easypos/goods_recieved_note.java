    private void GRN_NO() {

        try {
            ResultSet rs = MC_DB.search_dataQuery("select max(grn_no) from grn_details");
            if (rs.first()) {
                if (rs.getString(1) != null) {
                    int i = Integer.parseInt(rs.getString(1));
                    lbl_grn_no.setText(i + 1 + "");
                } else {
                    lbl_grn_no.setText("1001");
                }

            } else {
                lbl_grn_no.setText("1001");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
            e.printStackTrace();
            }
        }
    }

