    private void set_margine_marcup() {
        if (!txt_cost.getText().trim().isEmpty()) {
            double cost = Double.parseDouble(txt_cost.getText().trim());
            double sale = Double.parseDouble(txt_sale.getText().trim());
            double ans = sale - cost;
            double markup = ans / cost * 100;
            lbl_profit_amount.setText(new DecimalFormat("#,##0.00").format(ans));
            lbl_profit_percent.setText(new DecimalFormat("0.00").format(markup) + "%");
        } else {
            lbl_profit_amount.setText("0.00");
            lbl_profit_percent.setText("0.00");
        }
    }
