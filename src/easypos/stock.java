    public void tbups() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MC_DB.search_dataQuery("SELECT * FROM stock");
            NumberFormat numberFormat = new DecimalFormat("#,##0.00");
            while (rs.next()) {

                Vector v = new Vector();

                v.add(rs.getString("id"));
                v.add(rs.getString("code"));
                v.add(rs.getString("name"));
                v.add(rs.getString("s_code"));
                v.add(rs.getString("brand"));
                v.add(rs.getString("supplier"));
                v.add(numberFormat.format(rs.getDouble("buy_price")));
                v.add(numberFormat.format(rs.getDouble("sell_price")));
                v.add(rs.getString("rack"));
                v.add(numberFormat.format(rs.getDouble("reorder")));
                v.add(numberFormat.format(rs.getDouble("qty")));
                v.add(rs.getString("type"));

                dtm.addRow(v);

            }
            DefaultTableCellRenderer customRenderer = new DefaultTableCellRenderer() {
                @Override
                public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                    java.awt.Component cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                    DefaultTableModel model = (DefaultTableModel) table.getModel();
                    double reorder = Double.parseDouble(model.getValueAt(row, 9).toString().replace(",", ""));
                    double qty = Double.parseDouble(model.getValueAt(row, 10).toString().replace(",", ""));

                    // Set the foreground color for the entire row based on the condition
                    if (reorder > qty) {
                        cell.setForeground(Color.RED);
                    } else {
                        cell.setForeground(Color.BLACK);
                    }

                    // Right align specific columns
                    if (column == 6 || column == 7 || column == 8 || column == 9 || column == 10) {
                        setHorizontalAlignment(DefaultTableCellRenderer.RIGHT);
                    } else {
                        setHorizontalAlignment(DefaultTableCellRenderer.LEFT);
                    }

                    return cell;
                }
            };

            // Apply the custom renderer to all columns
            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setCellRenderer(customRenderer);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                MC_DB.myConnection().close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }

